-- ============================================================
-- RLS Policies for fs_orders and fs_order_items
-- Users can only read their own orders/items.
-- Edge Functions use service_role key (bypasses RLS) to write.
-- ============================================================

-- Enable RLS on fs_orders
ALTER TABLE fs_orders ENABLE ROW LEVEL SECURITY;

-- Users can read only their own orders (matched by email from auth.jwt())
CREATE POLICY "Users can read own orders"
  ON fs_orders
  FOR SELECT
  USING (
    email = (auth.jwt() ->> 'email')
  );

-- Enable RLS on fs_order_items
ALTER TABLE fs_order_items ENABLE ROW LEVEL SECURITY;

-- Users can read items of their own orders
CREATE POLICY "Users can read own order items"
  ON fs_order_items
  FOR SELECT
  USING (
    order_id IN (
      SELECT id FROM fs_orders WHERE email = (auth.jwt() ->> 'email')
    )
  );

-- ============================================================
-- Storage bucket for invoices
-- ============================================================

-- Create the invoices bucket (private by default)
INSERT INTO storage.buckets (id, name, public)
VALUES ('invoices', 'invoices', false)
ON CONFLICT (id) DO NOTHING;

