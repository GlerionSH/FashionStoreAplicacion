-- ============================================================
-- Ensure fs_orders has needed columns (idempotent)
-- ============================================================

-- Add stripe columns if they don't exist
DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_name = 'fs_orders' AND column_name = 'stripe_session_id'
  ) THEN
    ALTER TABLE fs_orders ADD COLUMN stripe_session_id text;
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_name = 'fs_orders' AND column_name = 'stripe_payment_intent'
  ) THEN
    ALTER TABLE fs_orders ADD COLUMN stripe_payment_intent text;
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_name = 'fs_orders' AND column_name = 'invoice_token'
  ) THEN
    ALTER TABLE fs_orders ADD COLUMN invoice_token text;
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_name = 'fs_orders' AND column_name = 'paid_at'
  ) THEN
    ALTER TABLE fs_orders ADD COLUMN paid_at timestamptz;
  END IF;
END $$;
